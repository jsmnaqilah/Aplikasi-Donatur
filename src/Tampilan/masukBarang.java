    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String sql = "INSERT INTO barang values(?,?,?,?,?,?,?,?,?)";
        try {
            PreparedStatement stat = con.prepareStatement(sql);
            stat.setString(1, tIdBarang.getText());
            stat.setString(2, tTgl.getText());
            stat.setString(3, tIdDonatur.getText());
            stat.setString(4, tNamaDonatur.getText());
            stat.setString(5, tIdPengelola.getText());
            stat.setString(6, tNamaPengelola.getText());
            stat.setString(7, cbJenis.getSelectedItem().toString());
            stat.setString(8, tJumlah.getText());
            stat.setString(9, tKeterangan.getText());

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil di Simpan");
            kosong();
            tIdBarang.requestFocus();
            datatable();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal di Simpan"+e);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

