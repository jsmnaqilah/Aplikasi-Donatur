    protected void datatable() {
        Object[] Baris={"ID Barang","Tanggal Barang diterima","ID Donatur","Nama Donatur",
            "ID Pengelola","Nama Pengelola","Jenis","Jumlah","Keterangan"};
        tabmode=new DefaultTableModel(null,Baris);
        tabelBarang.setModel(tabmode);
        String sql="SELECT * FROM barang";
        
        try{
            java.sql.Statement stat=con.createStatement();
            ResultSet hasil=stat.executeQuery(sql);
            while(hasil.next()){
                String a = hasil.getString("idBarang");
                String b = hasil.getString("tgl");
                String c = hasil.getString("idDonatur");
                String d = hasil.getString("namaDonatur");
                String e = hasil.getString("idPengelola");
                String f = hasil.getString("namaPengelola");
                String g = hasil.getString("jenisBarang");
                String h = hasil.getString("jumlahBarang");
                String i = hasil.getString("keterangan");
            
                String[] data={a,b,c,d,e,f,g,h,i};
                tabmode.addRow(data);
            }
        }catch(Exception e){
        }
    }

