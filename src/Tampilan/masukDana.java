    private void tabelDanaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelDanaMouseClicked
        // TODO add your handling code here:
        int bar = tabelDana.getSelectedRow();
        String a = tabmode.getValueAt(bar, 0).toString();
        String b = tabmode.getValueAt(bar, 1).toString();
        String c = tabmode.getValueAt(bar, 2).toString();
        String d = tabmode.getValueAt(bar, 3).toString();
        String e = tabmode.getValueAt(bar, 4).toString();
        String f = tabmode.getValueAt(bar, 5).toString();
        String g = tabmode.getValueAt(bar, 6).toString();
        String h = tabmode.getValueAt(bar, 7).toString();
        String i = tabmode.getValueAt(bar, 8).toString();

        tIdDana.setText(a);
        tTgl.setText(b);
        tIdPengelola.setText(c);
        tNamaPengelola.setText(d);
        tIdDonatur.setText(e);
        tNamaDonatur.setText(f);
        tNominal.setText(g);
        if(h.equals("Cash")){
            rCash.setSelected(true);
            rTf.setSelected(false);
        }else {
            rCash.setSelected(false);
            rTf.setSelected(true);
        }
        tKeterangan.setText(i);
    }//GEN-LAST:event_tabelDanaMouseClicked

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String sql = "INSERT INTO dana_asli values(?,?,?,?,?,?,?,?,?)";
        try {
            PreparedStatement stat = con.prepareStatement(sql);
            stat.setString(1, tIdDana.getText());
            stat.setString(2, tTgl.getText());
            stat.setString(3, tIdPengelola.getText());
            stat.setString(4, tNamaPengelola.getText());
            stat.setString(5, tIdDonatur.getText());
            stat.setString(6, tNamaDonatur.getText());
            stat.setString(7, tNominal.getText());
            String bentuk = "";
            if(rCash.isSelected()) bentuk = "Cash";
            else bentuk = "Transfer";
            stat.setString(8, bentuk);
            stat.setString(9, tKeterangan.getText());

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil di Simpan");
            kosong();
            tIdDana.requestFocus();
            datatable();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal di Simpan"+e);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

