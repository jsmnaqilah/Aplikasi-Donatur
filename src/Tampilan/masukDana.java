    protected void datatable() {
        Object[] Baris={"ID Dana","Tanggal Dana diterima","ID Pengelola","Nama Pengelola",
            "ID Donatur","Nama Donatur","Nominal","Bentuk","Keterangan"};
        tabmode=new DefaultTableModel(null,Baris);
        tabelDana.setModel(tabmode);
        String sql="SELECT * FROM dana_asli";
        
        try{
            java.sql.Statement stat=con.createStatement();
            ResultSet hasil=stat.executeQuery(sql);
            while(hasil.next()){
                String a = hasil.getString("idDana");
                String b = hasil.getString("tgl");
                String c = hasil.getString("idPengelola");
                String d = hasil.getString("namaPengelola");
                String e = hasil.getString("idDonatur");
                String f = hasil.getString("namaDonatur");
                String g = hasil.getString("nominal");
                String h = hasil.getString("bentuk");
                String i = hasil.getString("keterangan");
            
                String[] data={a,b,c,d,e,f,g,h,i};
                tabmode.addRow(data);
            }
        }catch(Exception e){
            
        }
    }
